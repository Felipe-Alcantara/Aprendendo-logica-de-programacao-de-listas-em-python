# Declaração de uma tupla 'indice' com os valores (1, 2, 3, 0, 4, 5)
indice = (1, 2, 3, 0, 4, 5)

# Declaração de uma lista 'semana' com os valores ["seg", "ter", "qua", "dom", "sex", "sab"]
semana = ["seg", "ter", "qua", "dom", "sex", "sab"]

# Loop 'for' que percorre cada valor em 'indice'
for i in indice:
    # Usa o valor atual de 'i' como índice para acessar o elemento correspondente em 'semana'
    # Imprime o dia da semana correspondente
    print (semana[i])